RSpec.feature "Switch Turns", :type => :feature do

  scenario "Warrior 1 attacks Warrior 2 and ends his turn" do
    sign_in_and_play
    expect(page).to have_text("Warrior1's turn")
    click_button("Attack Warrior2")
    expect(page).to have_text("Warrior2 health: 90")
    click_button("End Warrior1's turn")
    expect(page).to have_text("Warrior2's turn")
    expect(page).to_not have_button("Attack Warrior2")
  end

end
